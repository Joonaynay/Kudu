import UIKit

extension UIColor {
    
    static let theme = ColorTheme()
}

struct ColorTheme {
    
    let background = UIColor(named: "BackgroundColor")!
    let secondaryText = UIColor(named: "SecondaryTextColor")!
    let blueColor = UIColor(named: "BlueColor")!
    let tabBarColor = UIColor(named: "TabBarColor")!
    let accentColor = UIColor(named: "AccentColor")!
    let lineColor = UIColor(named: "LineColor")!
    let blueTextColor = UIColor(named: "BlueTextColor")!
}
